#include <iostream>
using namespace std;
int main(){
    // Post Increment 
    int x = 10;
    cout<< x << endl;
    cout << x++ << endl;
    cout << x << endl;

    // Pre Increment 
    int y = 5;
    cout << y << endl;
    cout << ++y << endl;
    cout << y << endl;

    return 0;
}